var x = 10;
var y = 3;
console.log("x =",x, "y =", y);
console.log("\n");
console.log("산술 연산자 +, - , *, / , %");
console.log("x + y = ", x+y);
console.log("x - y = ", x-y);
console.log("x * y = ", x*y);
console.log("x / y = ", x/y);
console.log("x % y = ", x%y);
console.log("\n");

console.log("대입 연산자");
console.log("x += y -> x = ", x+=y);
console.log("x -= y -> x = ", x-=y);
console.log("x *= y -> x = ", x*=y);
console.log("x /= y -> x = ", x/=y);
console.log("x %= y -> x = ", x%=y);
console.log("\n");

console.log("증감 연산자");
console.log("x++ -> x = ", x++);
console.log("x-- -> x = ", x--);
console.log("++x -> x = ", ++x);
console.log("--x -> x = ", --x);
console.log("\n");

